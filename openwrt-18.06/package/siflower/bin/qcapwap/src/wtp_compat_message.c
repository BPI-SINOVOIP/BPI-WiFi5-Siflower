int cwmsg_assemble_wtp_ipv4_addr(struct cw_ctrlmsg *msg)
{
	uint32_t addr;

	addr = get_ipv4_addr(NETWORK_IF);
	return cwmsg_ctrlmsg_add_raw_element(msg, CW_ELEM_LOCAL_IPV4_ADDRESS_CW_TYPE,
					     sizeof(addr), &addr);
}

int cwmsg_assemble_radio_info(struct cw_ctrlmsg *msg)
{
	struct message message;
	uint8_t buff[5] = {0};

	message.data = buff;
	message.len = sizeof(buff);

	buff[0] = WIFI_2G;
	buff[4] = PHY_STANDARD_G;
	if (cwmsg_ctrlmsg_add_element(msg, CW_ELEM_IEEE80211_WTP_RADIO_INFORMATION_CW_TYPE,
				      &message, 0))
		return -ENOMEM;
	buff[0] = WIFI_5G;
	buff[4] = PHY_STANDARD_A;
	if (cwmsg_ctrlmsg_add_element(msg, CW_ELEM_IEEE80211_WTP_RADIO_INFORMATION_CW_TYPE,
				      &message, 0))
		return -ENOMEM;
	return 0;
}

int cwmsg_assemble_frame_tunnel_mode(struct cw_ctrlmsg *msg)
{
	uint8_t mode = 2;

	return cwmsg_ctrlmsg_add_raw_element(msg, CW_ELEM_WTP_FRAME_TUNNEL_MODE_CW_TYPE,
					     sizeof(mode), &mode);
}

