int cwmsg_send_configure_request(struct capwap_ap *ap)
{
	struct iovec io;

	configure_request[12] = wtp_get_seq_num(ap);
	io.iov_base = configure_request;
	io.iov_len = sizeof(configure_request);

	return capwap_send_message(ap->ctrl_sock, &io, 1, NULL, 0);
}